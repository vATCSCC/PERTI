function normPlay($n) { return strtoupper(preg_replace('/[^A-Z0-9]/i', '', $n)); }

/**
 * Parse origin/destination from route string.
 * First token: K-prefix = airport, Z-prefix/CZY/N90 = ARTCC, UNKN = various
 * Last token: same logic
 */
function parseOriginDest($route_str) {
    $tokens = preg_split('/\s+/', trim($route_str));
    $first = $tokens[0];
    $last = end($tokens);

    $orig_apt = ''; $orig_artcc = '';
    $dest_apt = ''; $dest_artcc = '';

    if (preg_match('/^K[A-Z]{3}$/', $first)) {
        $orig_apt = $first;
    } elseif (preg_match('/^(Z[A-Z]{2}|CZ[A-Z]|N90)$/', $first)) {
        $orig_artcc = $first;
    }

    if (preg_match('/^K[A-Z]{3}$/', $last)) {
        $dest_apt = $last;
    } elseif (preg_match('/^(Z[A-Z]{2}|CZ[A-Z]|N90)$/', $last)) {
        $dest_artcc = $last;
    }

    $origin = $orig_apt ?: ($orig_artcc ?: ($first === 'UNKN' ? 'VARIOUS' : $first));
    $dest   = $dest_apt ?: ($dest_artcc ?: ($last === 'UNKN' ? 'VARIOUS' : $last));

    return [$origin, $dest, $orig_apt, $orig_artcc, $dest_apt, $dest_artcc];
}


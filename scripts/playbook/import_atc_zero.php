// PARSE ROUTE DATA
// ============================================================================
$parsed_routes = [];
foreach (explode("\n", $raw_routes) as $line) {
    $line = trim($line);
    if (!$line) continue;
    if (!preg_match('/^(\d+)\x{2192}(.+)$/u', $line, $m)) continue;

    $num = (int)$m[1];
    $route_str = trim($m[2]);

    $play_key = null;
    foreach ($boundaries as [$start, $end, $key]) {
        if ($num >= $start && $num <= $end) {
            $play_key = $key;
            break;
        }
    }
    if (!$play_key) {
        echo "WARNING: Route #$num has no play mapping, skipping\n";
        continue;
    }

    [$origin, $dest, $orig_apt, $orig_artcc, $dest_apt, $dest_artcc] = parseOriginDest($route_str);

    $parsed_routes[] = [
        'play'        => $play_key,
        'route'       => $route_str,
        'origin'      => $origin,
        'dest'        => $dest,
        'orig_apt'    => $orig_apt,
        'orig_artcc'  => $orig_artcc,
        'dest_apt'    => $dest_apt,
        'dest_artcc'  => $dest_artcc,
    ];
}

$total_routes = count($parsed_routes);
$play_count = count($plays);
echo "ATC-Zero Import: $play_count plays, $total_routes routes parsed\n";
flush();

if ($total_routes !== 543) {
    echo "WARNING: Expected 543 routes, got $total_routes\n";
}

// ============================================================================
// CHECK FOR EXISTING DATA
// ============================================================================

// Check existing ATC_ZERO plays (our namespace)
$existing_atz = (int)$pdo->query("SELECT COUNT(*) FROM playbook_plays WHERE source='DCC' AND org_code='ATC_ZERO'")->fetchColumn();
$is_reimport = $existing_atz > 0;
echo $is_reimport ? "Re-import ($existing_atz existing ATC_ZERO plays will be replaced)\n" : "First import\n";

// Build set of existing Contingency route strings (from OTHER sources, not ATC_ZERO)
$existing_routes = [];
$stmt = $pdo->query("
    SELECT r.route_string
    FROM playbook_routes r
    JOIN playbook_plays p ON r.play_id = p.play_id
    WHERE p.category = 'Contingency'
      AND NOT (p.source = 'DCC' AND p.org_code = 'ATC_ZERO')
");
while ($row = $stmt->fetch(PDO::FETCH_ASSOC)) {
    $existing_routes[strtoupper(trim($row['route_string']))] = true;
}
$existing_count = count($existing_routes);
echo "Found $existing_count existing Contingency routes (from other sources)\n";
flush();

// ============================================================================
// IMPORT EXECUTION
// ============================================================================
$pdo->beginTransaction();

try {
    // Delete existing ATC_ZERO data (idempotent reimport)
    if ($is_reimport) {
        $pdo->exec("DELETE FROM playbook_changelog WHERE play_id IN (SELECT play_id FROM playbook_plays WHERE source='DCC' AND org_code='ATC_ZERO')");
        $pdo->exec("DELETE FROM playbook_plays WHERE source='DCC' AND org_code='ATC_ZERO'");
        echo "Deleted existing ATC_ZERO data\n";
        flush();
    }

    // Filter out routes that already exist in other Contingency plays
    $new_routes = [];
    $skipped = 0;
    foreach ($parsed_routes as $r) {
        $key = strtoupper(trim($r['route']));
        if (isset($existing_routes[$key])) {
            $skipped++;
        } else {
            $new_routes[] = $r;
        }
    }
    echo "Skipping $skipped routes already in existing Contingency plays\n";
    echo "Inserting " . count($new_routes) . " new routes\n";
    flush();

    // Count routes per play (for new routes only)
    $play_route_counts = [];
    foreach ($new_routes as $r) {
        $play_route_counts[$r['play']] = ($play_route_counts[$r['play']] ?? 0) + 1;
    }

    // Insert plays (only those that have at least one new route)
    $play_ids = [];
    $plays_inserted = 0;
    $play_stmt = $pdo->prepare("INSERT INTO playbook_plays
        (play_name, play_name_norm, display_name, description, category,
         impacted_area, facilities_involved, route_format,
         source, status, route_count, org_code, created_by, created_at)
        VALUES (?,?,?,?,?,?,?,'standard','DCC','active',?,'ATC_ZERO','import',NOW())");

    foreach ($plays as $pn => $pd) {
        $rc = $play_route_counts[$pn] ?? 0;
        if ($rc === 0) {
            echo "  Skipping play '$pn' (all routes already exist)\n";
            continue;
        }

        $play_stmt->execute([
            $pn,
            normPlay($pn),
            $pd['display_name'],
            $pd['description'],
            'Contingency',
            $pd['impacted_area'],
            $pd['facilities_involved'],
            $rc,
        ]);
        $play_ids[$pn] = (int)$pdo->lastInsertId();
        $plays_inserted++;
    }
    echo "Inserted $plays_inserted plays\n";
    flush();

    // Insert routes in batches
    $route_batch = [];
    $sort_counters = [];
    $routes_inserted = 0;

    foreach ($new_routes as $r) {
        $pn = $r['play'];
        $pid = $play_ids[$pn] ?? null;
        if (!$pid) continue;

        if (!isset($sort_counters[$pn])) $sort_counters[$pn] = 0;

        $route_batch[] = [
            $pid, $r['route'], $r['origin'], $r['dest'],
            $r['orig_apt'], $r['orig_artcc'],
            $r['dest_apt'], $r['dest_artcc'],
            $sort_counters[$pn]++
        ];

        if (count($route_batch) >= 100) {
            $vals = [];
            $params = [];
            foreach ($route_batch as $rb) {
                $vals[] = "(?,?,?,?,?,?,?,?,?)";
                $params = array_merge($params, $rb);
            }
            $pdo->prepare("INSERT INTO playbook_routes (play_id, route_string, origin, dest, origin_airports, origin_artccs, dest_airports, dest_artccs, sort_order) VALUES " . implode(',', $vals))->execute($params);
            $routes_inserted += count($route_batch);
            $route_batch = [];
        }
    }

    // Flush remaining
    if (count($route_batch) > 0) {
        $vals = [];
        $params = [];
        foreach ($route_batch as $rb) {
            $vals[] = "(?,?,?,?,?,?,?,?,?)";
            $params = array_merge($params, $rb);
        }
        $pdo->prepare("INSERT INTO playbook_routes (play_id, route_string, origin, dest, origin_airports, origin_artccs, dest_airports, dest_artccs, sort_order) VALUES " . implode(',', $vals))->execute($params);
        $routes_inserted += count($route_batch);
    }

    echo "Inserted $routes_inserted routes\n";
    flush();

    // Changelog entries
    $action = $is_reimport ? 'faa_reimport' : 'faa_import';
    if (!empty($play_ids)) {
        $cl_vals = [];
        $cl_params = [];
        foreach ($play_ids as $pn => $pid) {
            $cl_vals[] = "(?,'$action','import',NOW())";
            $cl_params[] = $pid;
        }
        $pdo->prepare("INSERT INTO playbook_changelog (play_id, action, changed_by, changed_at) VALUES " . implode(',', $cl_vals))->execute($cl_params);
    }

    $pdo->commit();
    echo "\nDone: $plays_inserted plays, $routes_inserted routes, $skipped skipped (already existed)\n";

    // Verify
    $verify = $pdo->query("SELECT COUNT(*) AS plays FROM playbook_plays WHERE source='DCC' AND org_code='ATC_ZERO'")->fetch();
    $verify_r = $pdo->query("SELECT COUNT(*) AS routes FROM playbook_routes WHERE play_id IN (SELECT play_id FROM playbook_plays WHERE source='DCC' AND org_code='ATC_ZERO')")->fetch();
    echo "Verify: {$verify['plays']} plays, {$verify_r['routes']} routes in DB\n";

    // Show per-play breakdown
    echo "\nPer-play breakdown:\n";
    $breakdown = $pdo->query("
        SELECT p.play_name, p.route_count,
               (SELECT COUNT(*) FROM playbook_routes r WHERE r.play_id = p.play_id) AS actual_routes
        FROM playbook_plays p
        WHERE p.source = 'DCC' AND p.org_code = 'ATC_ZERO'
        ORDER BY p.play_id
    ");
    while ($row = $breakdown->fetch(PDO::FETCH_ASSOC)) {
        echo "  {$row['play_name']}: {$row['actual_routes']} routes\n";
    }

} catch (Exception $e) {
    $pdo->rollBack();
    echo "FAILED (rolled back): " . $e->getMessage() . "\n";
}
